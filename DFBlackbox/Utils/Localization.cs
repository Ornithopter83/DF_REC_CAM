    public const string Korean = "KOR";
    public const string English = "ENG";

    private static string _language = Korean;

    public static string CurrentLanguage => _language;
    public static bool IsEnglish => string.Equals(_language, English, StringComparison.OrdinalIgnoreCase);

    public static void SetLanguage(string? language)
    {
        _language = string.Equals(language, English, StringComparison.OrdinalIgnoreCase)
            ? English
            : Korean;
    }

    public static string NormalizeLanguage(string? language) =>
        string.Equals(language, English, StringComparison.OrdinalIgnoreCase) ? English : Korean;

    public static string T(string key, params object[] args)
    {
        string text = IsEnglish && EnglishTexts.TryGetValue(key, out string? english)
            ? english
            : KoreanTexts.TryGetValue(key, out string? korean)
                ? korean
                : key;

        return args.Length == 0 ? text : string.Format(text, args);
    }

